public class CostError {

    private String baseDurError;
    private String baseCostError;
    private String unitCostError;

    public String getBaseDurError() {
        return baseDurError;
    }

    public void setBaseDurError(String baseDurError) {
        this.baseDurError = baseDurError;
    }

    public String getBaseCostError() {
        return baseCostError;
    }

    public void setBaseCostError(String baseCostError) {
        this.baseCostError = baseCostError;
    }

    public String getUnitCostError() {
        return unitCostError;
    }

    public void setUnitCostError(String unitCostError) {
        this.unitCostError = unitCostError;
    }
}
